#!/usr/bin/env node
/**
 * Copies PNGs from the Cursor assets drop folder into public/gallery/inspirasjon-{n}.png
 * starting at n=48, and generates src/lib/inspirationGalleryExtras.gen.ts with categories.
 *
 * Override source: GALLERY_ASSETS=/path/to/folder node scripts/sync-gallery-assets.mjs
 */
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.join(__dirname, '..');
const DEFAULT_ASSETS =
  '/Users/wahidrahmani/.cursor/projects/Users-wahidrahmani-Desktop-r-nningen-selskapslokale/assets';
const ASSETS = process.env.GALLERY_ASSETS || DEFAULT_ASSETS;
const OUT_DIR = path.join(ROOT, 'public/gallery');
const GEN_FILE = path.join(ROOT, 'src/lib/inspirationGalleryExtras.gen.ts');
const START_NUM = 48;

/** @returns {'wedding' | 'corporate' | 'private' | 'facilities'} */
function categoryFromFilename(name) {
  const n = name.toLowerCase();
  if (/dji_|inngang|natur|garden_|^forside|^banner|^felles|^webb|forslag/.test(n)) {
    return 'facilities';
  }
  if (/bedrift|corporate|konfer|julebord|firmatur|møte|mote\b/.test(n)) {
    return 'corporate';
  }
  if (/dåp|daap|navnefest|konfirm|minnestund|privat\b|bursdag/.test(n)) {
    return 'private';
  }
  return 'wedding';
}

function main() {
  if (!fs.existsSync(ASSETS)) {
    console.error('GALLERY_ASSETS folder not found:', ASSETS);
    process.exit(1);
  }
  if (!fs.existsSync(OUT_DIR)) {
    fs.mkdirSync(OUT_DIR, { recursive: true });
  }

  const files = fs
    .readdirSync(ASSETS)
    .filter((f) => f.toLowerCase().endsWith('.png'))
    .sort((a, b) => a.localeCompare(b, 'en'));

  if (files.length === 0) {
    console.error('No PNG files in', ASSETS);
    process.exit(1);
  }

  const entries = [];
  let num = START_NUM;
  let lastDest = '';
  for (const f of files) {
    const key = `inspirasjon-${num}`;
    const destName = `inspirasjon-${num}.png`;
    lastDest = destName;
    const dest = path.join(OUT_DIR, destName);
    fs.copyFileSync(path.join(ASSETS, f), dest);
    const category = categoryFromFilename(f);
    entries.push({
      key,
      src: `/gallery/${destName}`,
      category,
      sourceFile: f,
    });
    num += 1;
  }

  const lines = entries.map(
    (e) =>
      `  { key: '${e.key}', src: '${e.src}', category: '${e.category}' }, // ${e.sourceFile.replace(/'/g, "\\'")}`,
  );

  const ts = `/* eslint-disable */
// AUTO-GENERATED by scripts/sync-gallery-assets.mjs — do not edit by hand.
// Re-run: node scripts/sync-gallery-assets.mjs

export type InspirationGalleryExtraSlide = {
  key: string;
  src: string;
  category: 'wedding' | 'corporate' | 'private' | 'facilities';
};

export const inspirationGalleryExtraSlides: InspirationGalleryExtraSlide[] = [
${lines.join('\n')}
];
`;

  fs.writeFileSync(GEN_FILE, ts, 'utf8');
  console.log(`Wrote ${entries.length} files to ${OUT_DIR} (last: ${lastDest})`);
  console.log('Generated', GEN_FILE);
}

main();
